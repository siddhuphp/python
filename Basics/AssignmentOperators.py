# Assignment Operators in Python with Examples

# 1. Assignment (=)
a = 10
print("a =", a)  # Output: 10

# 2. Add AND (+=)
a += 5
print("a += 5:", a)  # Output: 15

# 3. Subtract AND (-=)
a -= 3
print("a -= 3:", a)  # Output: 12

# 4. Multiply AND (*=)
a *= 2
print("a *= 2:", a)  # Output: 24

# 5. Divide AND (/=)
a /= 4
print("a /= 4:", a)  # Output: 6.0

# 6. Modulus AND (%=)
a %= 5
print("a %= 5:", a)  # Output: 1.0

# 7. Exponent AND (**=)
a **= 3
print("a **= 3:", a)  # Output: 1.0

# 8. Floor Division AND (//=)
b = 17
b //= 3
print("b //= 3:", b)  # Output: 5

# 9. Bitwise AND (&=)
c = 7  # 0b111
c &= 3  # 0b011
print("c &= 3:", c)  # Output: 3

# 10. Bitwise OR (|=)
c |= 8  # 0b1000
print("c |= 8:", c)  # Output: 11

# 11. Bitwise XOR (^=)
c ^= 2  # 0b10
print("c ^= 2:", c)  # Output: 9

# 12. Bitwise Right Shift (>>=)
c >>= 1
print("c >>= 1:", c)  # Output: 4

# 13. Bitwise Left Shift (<<=)
c <<= 2
print("c <<= 2:", c)  # Output: 16